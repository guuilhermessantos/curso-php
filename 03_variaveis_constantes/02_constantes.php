<?php

// ===== CONSTANTES =====
// Em PHP, constantes também podem ser declaradas com 'define', diferente do 'const' usado para variáveis constantes em JavaScript.
// Em PHP:
define('NOME', "João");
define('IDADE', 25);

// Em JavaScript seria:
// const NOME = "João";
// const IDADE = 25;

// Ou seja, a sintaxe é parecida, mas em PHP elas são realmente constantes, enquanto no JS é uma variável cujo valor não pode ser alterado.